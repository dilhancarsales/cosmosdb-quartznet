using System.IO;
using System.Runtime.Serialization.Formatters.Binary;

namespace Quartz.Spi.CosmosDbJobStore.Tests
{
    /// <summary>
    /// Generic extension methods for objects.
    /// </summary>
    public static class ObjectExtensions
    {
        /// <summary>
        /// Creates a deep copy of object by serializing to memory stream.
        /// </summary>
        /// <param name="obj"></param>
        public static T DeepClone<T>(this T obj) where T : class
        {
            if (obj == null)
            {
                return null;
            }

#pragma warning disable SYSLIB0011 // BinaryFormatter is obsolete but acceptable for test code
            BinaryFormatter bf = new BinaryFormatter();
            using (MemoryStream ms = new MemoryStream())
            {
                bf.Serialize(ms, obj);
                ms.Seek(0, SeekOrigin.Begin);
                return (T)bf.Deserialize(ms);
            }
#pragma warning restore SYSLIB0011
        }
    }
}
